def merge_sort(line):
    if len(line) < 2:
        return line[:]
    else:
        middle = len(line) // 2
        left = merge_sort(line[:middle])
        right = merge_sort(line[middle:])
        return merge(left, right)


def merge(left, right):
    result = []
    i, j = 0, 0
    while i < len(left) and j < len(right):
        if left[i] < right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1

    while i < len(left):
        result.append(left[i])
        i += 1

    while j < len(right):
        result.append(right[j])
        j += 1

    return result
